//46) Crie um programa que calcule e 
//mostre na tela o resultado da soma entre 6 + 
//8 + 10 + 12 + 14 + ... + 98 + 100

public class SomaEntreNumero {
public static void main(String[] args) {
	
	
	
	 for (int i=6; i<=100; ++i){	 
		 
		 System.out.println(i++);
		//++ (antes incrementa depois printa)
		// ++ (depois mostra na tela depois encrementa).
	}
			
	System.out.print("Acabou a contagem!! ");
}	
}